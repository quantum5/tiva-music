    /*  represents 8 vertical pixels and consecutive bytes give adjacent    */
    /*  sets of 8 vertical pixels progressing to the right across the       */
    /*  display. This is rotated 90 degrees from the conventional video     */
    /*  display orientation.                                                */
    /*                                                                      */
